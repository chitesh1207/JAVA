 //PRIMITIVE DATATYPE
public class PrimitiveDt
  {
     public static void main(String args[])
      {
       	 System.out.println("Data types integer values");

	 long l = 5l; //8 bytes 

         System.out.println("the value of l 8 bytes long" +l );  

         int i = 5;  //4 byte 

         System.out.println("the value of i 4 bytes int" +i );
         
         short s = 5;  //2 bytes
         
 	 System.out.println("the value of s 2 bytes short " +s );
	 
	 byte b = 5;  //1 byte
	 
	 System.out.println("the value of b 1 byte " +b );
	
	 System.out.println("Data types for decimal values"); 
        
	 float f = 34.5f;//4 bytes
         
	 System.out.println("the value of f 4 bytes" +f );
	 
	 double d = 34.5;//8 bytes
	
	 System.out.println("the value of d is 8 bytes "+f );
         
	 System.out.println("Data types for character");
	
	 char c = 'c';
   
	 System.out.println("the character c is"+c);
        
	 System.out.print("Data types for strings");
     
	 String st = "chitesh";
 
	 System.out.println("the string is " +st); 
       }
}

 
 	 
         
         
         