class Variables
{
  public static void main(String args[])
  {
   	 int i = 25; // "int" is a data type and "i" is a variable , "25" is a value
 
	 i = 4;      // changing int value from 25 to 4

   	 double j = 1423.5;// "double" is a data type which is used for point  values
 
   	 j = 6.7;       //changing double value from 1423 to 6.7
   
  	 char k = 'c';  //"char" is character data type 

   	 k = 'm';       // changing  char value from c to m

  	 float f = 2.5f; //  "float" is data type for floating point numbers 

    
  	 String s = "chitesh";  //"string" is data type used for names
      

		System.out.println(i);

		System.out.println(j);

		System.out.println(k);

		System.out.println(f);

		System.out.println(s);
    }
 
} 